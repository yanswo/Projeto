    public CarregandoTela() {
        initComponents();
        
        
        Timer timer = new Timer(50, new ActionListener() {
            int progress = 0;
            @Override
            public void actionPerformed(ActionEvent e) {
             if (progress < 100) {
                    progress += 1; // aumentar o progresso da barra (pode ajustar o valor aqui)
                    jProgressBar.setValue(progress); // atualiza o valor da barra de progresso
                } else {
                    
                    ((Timer) e.getSource()).stop(); 
                    dispose(); 
                    Cadastro cadastro = new Cadastro(); 
                    cadastro.setVisible(true); 
                }
            }
        });
        
       
        timer.setRepeats(true); 
        timer.start(); 
    }

 
   
